public class VariableNamesTheme {
    public static void main(String[] args) {
        // Первый блок с переменными
        int header1 = 1;
        System.out.println(header1 + ". " + "Разные переменные");

        // Цифра
        byte digit = 1;
        System.out.println("Цифра: " + digit);

        // Сумма чисел
        int sumNumbers = 2234;
        System.out.println("Сумма чисел = " + sumNumbers);

        // Произведение цифр
        byte numbersMultiply = 0;
        System.out.println("Произведение цифр = " + numbersMultiply);

        // Максимальное число
        long maxNumber = 1234567891987724258L;
        System.out.println("Максимальное число: " + maxNumber);

        // Количество десятков
        int dozenNumber = 5;
        System.out.println("Количество десятков: " + dozenNumber);

        // Вес собаки
        float dogWeight = 6.4f;
        System.out.println("Вес собаки = " + dogWeight + " кг ");

        // Исходное число
        int originalNumber = 7;
        System.out.println("Исходное число: " + originalNumber);

        // Процент по вкладу
        float depositPersent = 33.89f;
        System.out.println("Процент по вкладу: " + depositPersent + "%");

        // Переменная хранит символ &
        char ampersandSymbol = '&';
        System.out.println("Амперсанд: " + ampersandSymbol);

        // Код ошибки
        int errorCode = 404;
        System.out.println("Код ошибки: " + errorCode);

        // Тип сообщения
        String messageType = "смс";
        System.out.println("Тип сообщения: " + messageType);

        // Число нулей
        int zeroNumber = 18;
        System.out.println("Число нулей: " + zeroNumber);

        // Уникальное число
        int uniqueNumber = 99999;
        System.out.println("Уникальное число: " + uniqueNumber);

        // Случайное число
        int randomNumber = 9876543;
        System.out.println("Случайное число: " + randomNumber);

        // Математическое выражение
        String mathExp = "2+3=5";
        System.out.println("Математическое выражение: " + mathExp);

        // Счёт в игре
        String score = "1:1";
        System.out.println("Счёт в игре: " + score);

        // Максимальная длина
        int maxLength = 90;
        System.out.println("Максимальная длина = " + maxLength + " м");

        // Пункт меню
        String menu5 = "Контакты";
        System.out.println("Пункт меню: " + menu5);

        // Стоимость кофе
        float coffe = 14.99f;
        System.out.println("Стоимость кофе: " + coffe + "$");

        // Дата начала
        String startDate = "07.09.2024";
        System.out.println("Начало обучения курса StartJava: " + startDate);

        // Окончание диапазона
        int endRange = 500;
        System.out.println("Окончание диапазона: " + endRange);

        // Полное имя работника месяца
        String nameBestWorker = "Шойгу Сергей Кожеедович";
        System.out.println("Лучший работник месяца: " + nameBestWorker);

        // Заголовок электронной книги
        String bookTitle = "\"Знакомство с Java\"";
        System.out.println("Заголовок электронной книги: " + bookTitle + ".");

        // Размер
        String sizePhoto = "3x4";
        System.out.println("Фото на паспорт: " + sizePhoto + " cм");

        // Вместимость
        int capacity = 475;
        System.out.println("Вместимость багажника авто: " + capacity + " литров");

        // Счётчик
        String electricMeter = "0004.23";
        System.out.println("Энергопотребление: " + electricMeter + " kW/h");

        // Путь до файла
        String filePath = "C:\\Program Files\\Java\\StartJava";
        System.out.println("Путь до файла: " + filePath);

        // Количество чисел в каждой строке
        int numbersInRow = 18;
        System.out.println("Количество чисел в каждой строке: " + numbersInRow);
        System.out.println();

        // Второй блок с переменными
        int header2 = 2;
        System.out.println(header2 + ". " + "Boolean-переменные");

        // Сотни равны?
        boolean hundredsEquality = true;
        System.out.println("Сотни равны? " + "= " + hundredsEquality);

        // Компьютер включен?
        boolean computerOn = true;
        System.out.println("Компьютер включен? " + "= " + computerOn);

        // Есть равные цифры?
        boolean equalDigit = false;
        System.out.println("Есть равные цифры? " + "= " + equalDigit);

        // Создано?
        boolean created = true;
        System.out.println("Создано? " + "= " + created);

        // Пустое?
        boolean empty = false;
        System.out.println("Пустое? " + "= " + empty);

        // Активное?
        boolean active = true;
        System.out.println("Активное? " + "= " + active);

        // Новое?
        boolean new1 = true;
        System.out.println("Новое? " + "= " + new1);

        // Электронная почта действует?
        boolean emailCurrent = true;
        System.out.println("Электронная почта действует? " + "= " + emailCurrent);

        // Имеются уникальные строки?
        boolean uniqueStrings = false;
        System.out.println("Имеются уникальные строки? " + "= " + uniqueStrings);
        System.out.println();

        // Третий блок с переменными
        int header3 = 3;
        System.out.println(header2 + ". " + "Аббревиатуры");

        // Старый universally unique identifier
        String uuid = "UUID";
        System.out.println("Старый universally unique identifier: " + uuid);

        // Производитель оперативной памяти
        String mor = "ПОП";
        System.out.println("Производитель оперативной памяти: " + mor);

        // Ёмкость жесткого диска
        String hdc = "HDC";
        System.out.println("Ёмкость жесткого диска: " + hdc);

        // Протокол передачи гипертекста
        String http = "HTTP";
        System.out.println("Протокол передачи гипертекста: " + http);

        // Сокращенный uniform resource locator
        String url = "URL";
        System.out.println("Сокращенный uniform resource locator: " + url);

        // Новый идентификатор клиента
        String ncid = "NCID";
        System.out.println("Новый идентификатор клиента: " + ncid);

        // American standard code for information interchange
        String ascii = "ASCII";
        System.out.println("American standard code for information interchange: " + ascii);
    }
}